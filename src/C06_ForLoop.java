public class C06_ForLoop {
    public static void main(String[] args) {
       int input=10;
        for (int i = 1; i <=input; i++) {
            for (int j = 1; j <=input; j++) {
                System.out.println(i+"*"+j+"="+i*j);

            }
            System.out.println(" ");

        }

    }
}
