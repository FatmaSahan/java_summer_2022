package Practice_1;

public class C03_PrimitiveDataTyps {
    public static void main(String[] args) {
        /*
        double > float > long > int > short > byte
        bunlarin max ve min degerlerini bulalim
         */
        int intMax=Integer.MAX_VALUE;
        int intMin=Integer.MIN_VALUE;
        System.out.println("intMax:"+intMax);
        System.out.println("intMin:"+intMin);

        float fltMax=Float.MAX_VALUE;
        float fltMin=Float.MIN_VALUE;
        System.out.println("fltMin = " + fltMin);
        System.out.println("fltMax = " + fltMax);

        long longMax=Long.MAX_VALUE;
        long longMin=Long.MIN_VALUE;
        System.out.println("longin = " + longMin);
        System.out.println("longMax = " + longMax);


    }
}
